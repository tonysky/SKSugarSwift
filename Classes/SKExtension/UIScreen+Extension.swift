import UIKit

extension UIScreen {
    
    class func gx_screenWidth() -> CGFloat {
        return main.bounds.size.width
    }
    
    class func gx_screenHeight() -> CGFloat {
        return main.bounds.size.height
    }
    
    class func gx_scale() -> CGFloat {
        return main.scale;
    }
    
    
    
    
    

}










